// String Methods
// JavaScript provides various methods to manipulate and interact with strings. 
// Here are some of the most common string methods:


// 1.)  length: Returns the length of the string.
// let str = "Hello, World!";
// console.log(str.length); // Output: 13

// 2.) toUpperCase(): Converts the string to uppercase letters.
// let str = "Hello, World!";
// console.log(str.toUpperCase()); // Output: "HELLO, WORLD!"

// 3.) toLowerCase(): Converts the string to lowercase letters.
// let str = "Hello, World!";
// console.log(str.toLowerCase()); // Output: "hello, world!"

// 4.) charAt(index): Returns the character at the specified index.
// let str = "Hello, World!";
// console.log(str.charAt(0)); // Output: "H"

// 5.) indexOf(substring): Returns the index of the first occurrence of the specified substring. 
// Returns -1 if the substring is not found.
// let str = "Hello, World!";
// console.log(str.indexOf("World")); // Output: 7

// 6.) substring(start, end): Returns a portion of the string from the start index up to, 
// but not including, the end index.
// let str = "Hello, World!";
// console.log(str.substring(0, 5)); // Output: "Hello"

// 7.) slice(start, end): Similar to substring(), but it can accept negative indices. 
// Negative indices count from the end of the string.
// let str = "Hello, World!";
// console.log(str.slice(7, 12)); // Output: "World"
// console.log(str.slice(-6, -1)); // Output: "World"

// 8.) replace(searchValue, newValue): Replaces the first occurrence of searchValue with newValue.
// let str = "Hello, World!";
// console.log(str.replace("World", "JavaScript")); // Output: "Hello, JavaScript!"

// 9.) split(separator): Splits the string into an array of substrings based on the specified separator.
 // let str = "Hello, World!";
 // console.log(str.split(", ")); // Output: ["Hello", " World!"]

//  10.) trim(): Removes whitespace from both ends of the string.
// let str = "   Hello, World!   ";
// console.log(str.trim()); // Output: "Hello, World!"

// 11.) includes(substring): Checks if the string contains the specified substring. Returns true or false.
// let str = "Hello, World!";
// console.log(str.includes("World")); // Output: true

// 12.) endsWith(substring): Checks if the string ends with the specified substring. Returns true or false
// let str = "Hello, World!";
// console.log(str.endsWith("!")); // Output: true

// 13.) startsWith(substring): Checks if the string starts with the specified substring. Returns true or false.
// let str = "Hello, World!";
// console.log(str.startsWith("Hello")); // Output: true

